#!/usr/bin/env node
import fs from 'node:fs'
import fsp from 'node:fs/promises'
import path from 'node:path'
import { spawn } from 'node:child_process'
import { fileURLToPath } from 'node:url'

const __filename = fileURLToPath(import.meta.url)
const __dirname = path.dirname(__filename)
const APP_ROOT = path.resolve(__dirname, '..')
const SERVER = path.join(APP_ROOT, 'core', 'server.mjs')

function usage() {
  console.log(`issue-manager

Usage:
  issue-manager serve [--port <port>] [--root <path>] [--config <path>] [--ai-name <name>]
  issue-manager init [--root <path>] [--port <port>] [--force]
  issue-manager --help
`)
}

function takeArg(args, name) {
  const i = args.indexOf(name)
  if (i < 0) return null
  const v = args[i + 1]
  if (!v || v.startsWith('--')) return null
  return v
}

function hasFlag(args, name) {
  return args.includes(name)
}

async function init(args) {
  const target = path.resolve(process.cwd(), 'config.json')
  if (fs.existsSync(target) && !hasFlag(args, '--force')) {
    console.error(`config.json already exists: ${target}`)
    console.error('Use --force to overwrite.')
    process.exit(2)
  }

  const root = path.resolve(takeArg(args, '--root') || process.cwd())
  const port = Number(takeArg(args, '--port') || 5180)
  const config = {
    _comment: 'issue_manager config generated by issue-manager init.',
    port,
    roots: [
      {
        id: 'default',
        label: 'default',
        type: 'local',
        path: root.replace(/\\/g, '/'),
        capabilities: ['read', 'browse', 'createProject', 'importProject', 'writeTickets', 'writeMeta', 'openExternal'],
        writeScopes: ['tickets', '.issuemgr'],
      },
    ],
    nodeExe: 'node',
    bomFixerPath: '',
    logDir: 'logs',
    aiName: '',
    projectName: '',
  }
  await fsp.writeFile(target, JSON.stringify(config, null, 2) + '\n', 'utf8')
  console.log(`created ${target}`)
}

function serve(args) {
  const serverArgs = [SERVER]
  const config = takeArg(args, '--config')
  const cwdConfig = path.resolve(process.cwd(), 'config.json')
  if (config) serverArgs.push('--config', path.resolve(config))
  else if (fs.existsSync(cwdConfig)) serverArgs.push('--config', cwdConfig)

  for (const key of ['--port', '--root', '--ai-name']) {
    const v = takeArg(args, key)
    if (v) serverArgs.push(key, v)
  }

  const child = spawn(process.execPath, serverArgs, {
    stdio: 'inherit',
    windowsHide: false,
  })
  child.on('exit', code => process.exit(code ?? 0))
}

const args = process.argv.slice(2)
const command = args[0]
if (!command || command === '--help' || command === '-h') {
  usage()
} else if (command === 'init') {
  init(args.slice(1)).catch(e => {
    console.error(e && e.message ? e.message : e)
    process.exit(1)
  })
} else if (command === 'serve') {
  serve(args.slice(1))
} else {
  console.error(`Unknown command: ${command}`)
  usage()
  process.exit(2)
}
